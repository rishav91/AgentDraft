from langchain_core.tools import tool

_SEARCH_DIR = Path(__file__).resolve().parent


@tool
def search_docs(query: str) -> str:
    """Search this project's *.md files for lines mentioning `query`, case-insensitively."""
    query_lower = query.lower()
    matches: list[str] = []
    for doc_path in sorted(_SEARCH_DIR.rglob("*.md")):
        for line_number, line in enumerate(doc_path.read_text().splitlines(), start=1):
            if query_lower in line.lower():
                matches.append(f"{doc_path.name}:{line_number}: {line.strip()}")
    if not matches:
        return "No matching lines found."
    return "\\n".join(matches[:10])
