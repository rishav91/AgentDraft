PROVIDERS = ("anthropic", "openai")

#: Env var `agentdraft doctor` (3.5.3) also uses to check a schema's provider
#: against the environment - kept here since `init` owns the provider->key
#: association for the templates it generates.
PROVIDER_API_KEY_ENV = {
    "anthropic": "ANTHROPIC_API_KEY",
    "openai": "OPENAI_API_KEY",
}


class ScaffoldExistsError(Exception):
    """Raised when scaffold's target files already exist and force wasn't given."""

    def __init__(self, existing: list[str]) -> None:
        self.existing = existing
        super().__init__(f"file(s) already exist: {', '.join(existing)}")


def _files_for(provider: str) -> dict[str, str]:
    env_example = f"{PROVIDER_API_KEY_ENV[provider]}=\n"
    if provider == "anthropic":
        return {
            "schema.yaml": _ANTHROPIC_SCHEMA_YAML,
            "tools.py": _TOOLS_PY,
            "NOTES.md": _NOTES_MD,
            ".env.example": env_example,
        }
    return {
        "schema.yaml": _OPENAI_SCHEMA_YAML,
        "tools.py": _TOOLS_PY,
        "handlers.py": _HANDLERS_PY,
        "routing.py": _ROUTING_PY,
        "NOTES.md": _NOTES_MD,
        ".env.example": env_example,
    }


def scaffold(dest: Path, provider: str, force: bool) -> list[Path]:
    """Write a starter project for PROVIDER into DEST, creating it if needed.

    Raises ScaffoldExistsError (and writes nothing) if any target file
    already exists and FORCE is False.
    """
    files = _files_for(provider)
    existing = sorted(name for name in files if (dest / name).exists())
    if existing and not force:
        raise ScaffoldExistsError(existing)

    dest.mkdir(parents=True, exist_ok=True)
    written: list[Path] = []
    for name, content in files.items():
        path = dest / name
        path.write_text(content)
        written.append(path)
    return written
